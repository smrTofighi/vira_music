import 'package:flutter/material.dart';
import 'package:vira_music_app/gen/fonts.gen.dart';

class AppTheme {
  AppTheme._();
  static ThemeData lightTheme() {
    return ThemeData(
      fontFamily: FontFamily.samim,
      useMaterial3: true,
    );
  }

  static ThemeData darkTheme() {
    return ThemeData(
      fontFamily: FontFamily.samim,
      useMaterial3: true,
    );
  }
}
